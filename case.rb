weather = 'warm'


case weather
when 'snowing'
puts "Wear snow boots, hat, gloves, scarf, and down coat"
when 'hot'
puts "Wear ideally nothing"
when 'warm'
puts "Wear light layers, in light airy colors"
when 'chilly'
puts "Wear light layers, with a heavy sweater on top"
else 
puts "You're on your own."
end
